#include <iostream>
using namespace std;

int main() {
    //Goal: To see how integer division and modulo works
    cout << "10 / 3 = " << 10 / 3 << endl; //integer division
    cout << "10 % 3 = " << 10 % 3 << endl; //remainder
    cout << "10 / 3.0 = " << 10 / 3.0 << endl;
    cout << "10.0 / 3 = " << 10.0 / 3 << endl;
    cout << "10.0 / 3.0 = " << 10.0 / 3.0 << endl;

    return 0;
}
